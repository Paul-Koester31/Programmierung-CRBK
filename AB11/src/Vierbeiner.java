public abstract class Vierbeiner {
    private String name;
    private int gewicht;

    public void setName(String name) {
        this.name = name;
    }

    public void setGewicht(int gewicht) {
        this.gewicht = gewicht;
    }

    public int getGewicht() {
        return gewicht;
    }

    public String getName() {
        return name;
    }
}
